#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	size_t	i;

	dest = malloc((ft_strlen(src) + 1) * sizeof (char));
	if (!dest)
		return (NULL);
	i = 0;
	while (*(src + i))
	{
		*(dest + i) = *(src + i);
		i++;
	}
	*(dest + i) = '\0';
	return (dest);
}
